package PreplnstaOnlineQuiestionAndCodeProvide;

public class SumOfGivenRange {
    public static void main(String[] args) {
        int a=5,b=10;
        int sum=0;
        for (int i=a; i<=b; i++){
            sum +=i;
        }
        System.out.println("Sum of a and b : "+sum);
    }
}
